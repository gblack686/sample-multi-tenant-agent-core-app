def _add_docx_title_block(doc, title: str):
    """Add a branded title block at the top of the document."""
    from docx.shared import Pt, RGBColor
    from docx.enum.text import WD_ALIGN_PARAGRAPH

    # Title
    p_title = doc.add_paragraph()
    p_title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p_title.add_run(title)
    run.font.size = Pt(22)
    run.font.bold = True
    run.font.color.rgb = RGBColor(*NCI_BLUE_RGB)

    # Subtitle
    p_sub = doc.add_paragraph()
    p_sub.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run_sub = p_sub.add_run("National Cancer Institute (NCI)")
    run_sub.font.size = Pt(12)
    run_sub.font.color.rgb = RGBColor(80, 80, 80)

    # Date
    p_date = doc.add_paragraph()
    p_date.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run_date = p_date.add_run(f"Document Date: {datetime.utcnow().strftime('%B %d, %Y')}")
    run_date.font.size = Pt(10)
    run_date.font.color.rgb = RGBColor(120, 120, 120)
    run_date.font.italic = True

    # Status badge
    p_status = doc.add_paragraph()
    p_status.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run_status = p_status.add_run("DRAFT — FOR REVIEW ONLY")
    run_status.font.size = Pt(10)
    run_status.font.bold = True
    run_status.font.color.rgb = RGBColor(180, 80, 0)

    # Spacer
    doc.add_paragraph()


def _add_table_to_doc(doc, table_data: list):
    """Add a branded table to the DOCX document."""
    from docx.shared import Pt, RGBColor
    from docx.oxml.ns import qn
    from docx.oxml import OxmlElement

    if not table_data:
        return

    rows = len(table_data)
    cols = len(table_data[0]) if table_data else 0
    if rows == 0 or cols == 0:
        return

    table = doc.add_table(rows=rows, cols=cols)
    table.style = 'Table Grid'

    for i, row_data in enumerate(table_data):
        row = table.rows[i]
        for j, cell_text in enumerate(row_data):
            if j < len(row.cells):
                cell = row.cells[j]
                cell.text = cell_text

                # Style header row — NCI blue background, white bold text
                if i == 0:
                    shading = OxmlElement('w:shd')
                    shading.set(qn('w:val'), 'clear')
                    shading.set(qn('w:color'), 'auto')
                    shading.set(qn('w:fill'), '003366')
                    cell._element.get_or_add_tcPr().append(shading)

                    for paragraph in cell.paragraphs:
                        for run in paragraph.runs:
                            run.bold = True
                            run.font.color.rgb = RGBColor(*TABLE_HEADER_TEXT_RGB)
                            run.font.size = Pt(10)
                else:
                    # Alternate row shading
                    if i % 2 == 0:
                        shading = OxmlElement('w:shd')
                        shading.set(qn('w:val'), 'clear')
                        shading.set(qn('w:color'), 'auto')
                        shading.set(qn('w:fill'), 'E6EEF5')
                        cell._element.get_or_add_tcPr().append(shading)

                    for paragraph in cell.paragraphs:
                        for run in paragraph.runs:
                            run.font.size = Pt(9)


def _add_formatted_text(paragraph, text: str):
    """Add text with inline formatting (bold, italic, code) to a paragraph."""
    from docx.shared import Pt, RGBColor

    pattern = r'(\*\*.*?\*\*|\*.*?\*|`.*?`)'
    parts = re.split(pattern, text)

    for part in parts:
        if not part:
            continue
        if part.startswith('**') and part.endswith('**'):
            run = paragraph.add_run(part[2:-2])
            run.bold = True
        elif part.startswith('*') and part.endswith('*'):
            run = paragraph.add_run(part[1:-1])
            run.italic = True
        elif part.startswith('`') and part.endswith('`'):
            run = paragraph.add_run(part[1:-1])
            run.font.name = 'Courier New'
            run.font.size = Pt(9)
            run.font.color.rgb = RGBColor(80, 80, 80)
        else:
            paragraph.add_run(part)


def _docx_fallback(content: str, title: str) -> bytes:
    """Fallback when python-docx is not available."""
    header = f"EAGLE Document Export\nTitle: {title}\nGenerated: {datetime.utcnow().isoformat()}\n\n{'=' * 60}\n\n"
    return (header + content).encode('utf-8')


