logger = logging.getLogger("eagle.export")

# ── Brand constants ──────────────────────────────────────────────────
NCI_BLUE = "#003366"
NCI_BLUE_RGB = (0, 51, 102)
NCI_BLUE_LIGHT_RGB = (230, 238, 245)   # Light blue for table alt-rows
TABLE_HEADER_TEXT_RGB = (255, 255, 255) # White text on blue header
WATERMARK_TEXT = "DRAFT"
FOOTER_TEXT = "Generated by EAGLE — NCI Acquisition Assistant"


