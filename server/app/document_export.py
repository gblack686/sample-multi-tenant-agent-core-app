def markdown_to_docx(content: str, title: str = "Document") -> bytes:
    """Convert markdown content to a branded DOCX file. Returns bytes."""
    try:
        from docx import Document
        from docx.shared import Inches, Pt, RGBColor, Cm
        from docx.enum.text import WD_ALIGN_PARAGRAPH
        from docx.enum.table import WD_TABLE_ALIGNMENT
        from docx.oxml.ns import qn
    except ImportError:
        logger.warning("python-docx not installed, using fallback")
        return _docx_fallback(content, title)

    doc = Document()

    # ── Document properties ──────────────────────────────────────────
    doc.core_properties.title = title
    doc.core_properties.author = "EAGLE - NCI Acquisition Assistant"
    doc.core_properties.created = datetime.utcnow()

    # ── Page margins ─────────────────────────────────────────────────
    for section in doc.sections:
        section.top_margin = Cm(2.54)
        section.bottom_margin = Cm(2.54)
        section.left_margin = Cm(2.54)
        section.right_margin = Cm(2.54)

    # ── Custom heading styles (NCI blue) ─────────────────────────────
    _setup_docx_styles(doc)

    # ── Header / Footer ──────────────────────────────────────────────
    _add_docx_header_footer(doc, title)

    # ── DRAFT watermark ──────────────────────────────────────────────
    _add_docx_watermark(doc)

    # ── Title block ──────────────────────────────────────────────────
    _add_docx_title_block(doc, title)

    # ── Parse markdown body ──────────────────────────────────────────
    lines = content.split('\n')
    in_table = False
    table_data: List[List[str]] = []
    in_code_block = False
    code_lines: List[str] = []

    for line in lines:
        stripped = line.strip()

        # Code blocks
        if stripped.startswith('```'):
            if in_code_block:
                if code_lines:
                    p = doc.add_paragraph()
                    p.style = 'No Spacing'
                    run = p.add_run('\n'.join(code_lines))
                    run.font.name = 'Courier New'
                    run.font.size = Pt(9)
                    run.font.color.rgb = RGBColor(50, 50, 50)
                code_lines = []
                in_code_block = False
            else:
                in_code_block = True
            continue

        if in_code_block:
            code_lines.append(line)
            continue

        # Tables
        if stripped.startswith('|') and stripped.endswith('|'):
            if '---' in stripped:
                continue
            cells = [c.strip() for c in stripped.split('|')[1:-1]]
            if not in_table:
                in_table = True
                table_data = [cells]
            else:
                table_data.append(cells)
            continue
        elif in_table:
            _add_table_to_doc(doc, table_data)
            table_data = []
            in_table = False

        # Headers
        if stripped.startswith('# '):
            doc.add_heading(stripped[2:], level=1)
        elif stripped.startswith('## '):
            doc.add_heading(stripped[3:], level=2)
        elif stripped.startswith('### '):
            doc.add_heading(stripped[4:], level=3)
        elif stripped.startswith('#### '):
            doc.add_heading(stripped[5:], level=4)
        # Horizontal rules
        elif stripped in ('---', '***', '___'):
            p = doc.add_paragraph()
            p.paragraph_format.space_before = Pt(6)
            p.paragraph_format.space_after = Pt(6)
            run = p.add_run('─' * 60)
            run.font.color.rgb = RGBColor(180, 180, 180)
            run.font.size = Pt(8)
        # Checkboxes (MUST come before bullet check — '- [ ] ' starts with '- ')
        elif stripped.startswith('- [ ] '):
            p = doc.add_paragraph('☐  ' + stripped[6:], style='List Bullet')
        elif stripped.startswith('- [x] ') or stripped.startswith('- [X] '):
            p = doc.add_paragraph('☑  ' + stripped[6:], style='List Bullet')
        # Bullet points
        elif stripped.startswith('- ') or stripped.startswith('* '):
            doc.add_paragraph(stripped[2:], style='List Bullet')
        # Numbered lists
        elif re.match(r'^\d+\.\s', stripped):
            text = re.sub(r'^\d+\.\s', '', stripped)
            doc.add_paragraph(text, style='List Number')
        # Blockquotes
        elif stripped.startswith('> '):
            p = doc.add_paragraph(stripped[2:])
            p.style = 'Quote'
        # Empty lines
        elif not stripped:
            doc.add_paragraph()
        # Regular paragraphs
        else:
            p = doc.add_paragraph()
            _add_formatted_text(p, stripped)

    # Remaining table
    if in_table and table_data:
        _add_table_to_doc(doc, table_data)

    # Save
    buffer = io.BytesIO()
    doc.save(buffer)
    buffer.seek(0)
    return buffer.read()


def _setup_docx_styles(doc):
    """Apply NCI blue to heading styles."""
    from docx.shared import Pt, RGBColor

    blue = RGBColor(*NCI_BLUE_RGB)
    heading_sizes = {0: 20, 1: 16, 2: 14, 3: 12}  # Heading 1-4

    for level, size in heading_sizes.items():
        style = doc.styles[f'Heading {level + 1}']
        style.font.color.rgb = blue
        style.font.size = Pt(size)
        style.font.bold = True


def _add_docx_header_footer(doc, title: str):
    """Add branded header and footer with page numbers."""
    from docx.shared import Pt, RGBColor, Inches
    from docx.enum.text import WD_ALIGN_PARAGRAPH
    from docx.oxml.ns import qn
    from docx.oxml import OxmlElement

    section = doc.sections[0]

    # ── Header ───────────────────────────────────────────────────────
    header = section.header
    header.is_linked_to_previous = False
    hp = header.paragraphs[0]
    hp.alignment = WD_ALIGN_PARAGRAPH.LEFT

    run_eagle = hp.add_run("EAGLE")
    run_eagle.font.size = Pt(9)
    run_eagle.font.bold = True
    run_eagle.font.color.rgb = RGBColor(*NCI_BLUE_RGB)

    run_sep = hp.add_run("  |  ")
    run_sep.font.size = Pt(8)
    run_sep.font.color.rgb = RGBColor(150, 150, 150)

    run_title = hp.add_run(title[:60])
    run_title.font.size = Pt(8)
    run_title.font.color.rgb = RGBColor(100, 100, 100)

    # Thin line under header
    pPr = hp._element.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    bottom = OxmlElement('w:bottom')
    bottom.set(qn('w:val'), 'single')
    bottom.set(qn('w:sz'), '4')
    bottom.set(qn('w:space'), '4')
    bottom.set(qn('w:color'), '003366')
    pBdr.append(bottom)
    pPr.append(pBdr)

    # ── Footer ───────────────────────────────────────────────────────
    footer = section.footer
    footer.is_linked_to_previous = False
    fp = footer.paragraphs[0]
    fp.alignment = WD_ALIGN_PARAGRAPH.CENTER

    run_footer = fp.add_run(FOOTER_TEXT + "  |  Page ")
    run_footer.font.size = Pt(8)
    run_footer.font.color.rgb = RGBColor(120, 120, 120)

    # Page number field
    fldChar1 = OxmlElement('w:fldChar')
    fldChar1.set(qn('w:fldCharType'), 'begin')
    fp.add_run()._element.append(fldChar1)

    instrText = OxmlElement('w:instrText')
    instrText.text = ' PAGE '
    fp.add_run()._element.append(instrText)

    fldChar2 = OxmlElement('w:fldChar')
    fldChar2.set(qn('w:fldCharType'), 'end')
    fp.add_run()._element.append(fldChar2)


