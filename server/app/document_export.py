def markdown_to_pdf(content: str, title: str = "Document") -> bytes:
    """Convert markdown content to a branded PDF file. Returns bytes."""
    try:
        from reportlab.lib.pagesizes import letter
        from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
        from reportlab.lib.units import inch
        from reportlab.platypus import (
            SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle,
            PageBreak,
        )
        from reportlab.lib import colors
        from reportlab.lib.enums import TA_LEFT, TA_CENTER, TA_RIGHT
    except ImportError:
        logger.warning("reportlab not installed, using fallback")
        return _pdf_fallback(content, title)

    buffer = io.BytesIO()

    nci_blue = colors.Color(
        NCI_BLUE_RGB[0] / 255,
        NCI_BLUE_RGB[1] / 255,
        NCI_BLUE_RGB[2] / 255,
    )
    nci_light = colors.Color(
        NCI_BLUE_LIGHT_RGB[0] / 255,
        NCI_BLUE_LIGHT_RGB[1] / 255,
        NCI_BLUE_LIGHT_RGB[2] / 255,
    )

    # Page template callback for header/footer
    doc_title = title  # capture for closure

    def _on_page(canvas, doc):
        canvas.saveState()
        width, height = letter

        # ── Header line ──────────────────────────────────────────────
        canvas.setStrokeColor(nci_blue)
        canvas.setLineWidth(1.5)
        canvas.line(inch, height - 0.6 * inch, width - inch, height - 0.6 * inch)

        canvas.setFont('Helvetica-Bold', 8)
        canvas.setFillColor(nci_blue)
        canvas.drawString(inch, height - 0.52 * inch, "EAGLE")

        canvas.setFont('Helvetica', 7)
        canvas.setFillColor(colors.grey)
        canvas.drawString(inch + 42, height - 0.52 * inch, f"|  {doc_title[:55]}")

        # Date on right
        canvas.drawRightString(
            width - inch, height - 0.52 * inch,
            datetime.utcnow().strftime('%B %d, %Y'),
        )

        # ── Footer ───────────────────────────────────────────────────
        canvas.setStrokeColor(colors.Color(0.8, 0.8, 0.8))
        canvas.setLineWidth(0.5)
        canvas.line(inch, 0.65 * inch, width - inch, 0.65 * inch)

        canvas.setFont('Helvetica', 7)
        canvas.setFillColor(colors.grey)
        canvas.drawString(inch, 0.5 * inch, FOOTER_TEXT)
        canvas.drawRightString(width - inch, 0.5 * inch, f"Page {doc.page}")

        # ── DRAFT watermark ──────────────────────────────────────────
        canvas.setFont('Helvetica-Bold', 60)
        canvas.setFillColor(colors.Color(0.9, 0.9, 0.9))
        canvas.saveState()
        canvas.translate(width / 2, height / 2)
        canvas.rotate(45)
        canvas.drawCentredString(0, 0, WATERMARK_TEXT)
        canvas.restoreState()

        canvas.restoreState()

    doc = SimpleDocTemplate(
        buffer,
        pagesize=letter,
        rightMargin=inch,
        leftMargin=inch,
        topMargin=0.9 * inch,
        bottomMargin=0.9 * inch,
    )

    styles = getSampleStyleSheet()

    # ── Custom styles ────────────────────────────────────────────────
    styles.add(ParagraphStyle(
        name='EagleTitle',
        parent=styles['Title'],
        fontSize=22,
        textColor=nci_blue,
        spaceAfter=6,
        alignment=TA_CENTER,
    ))
    styles.add(ParagraphStyle(
        name='EagleSubtitle',
        parent=styles['Normal'],
        fontSize=11,
        textColor=colors.grey,
        alignment=TA_CENTER,
        spaceAfter=4,
    ))
    styles.add(ParagraphStyle(
        name='EagleStatus',
        parent=styles['Normal'],
        fontSize=10,
        textColor=colors.Color(0.7, 0.3, 0),
        alignment=TA_CENTER,
        spaceBefore=4,
        spaceAfter=20,
    ))
    styles.add(ParagraphStyle(
        name='EagleH1',
        parent=styles['Heading1'],
        fontSize=16,
        textColor=nci_blue,
        spaceAfter=12,
        spaceBefore=20,
        fontName='Helvetica-Bold',
    ))
    styles.add(ParagraphStyle(
        name='EagleH2',
        parent=styles['Heading2'],
        fontSize=14,
        textColor=nci_blue,
        spaceAfter=10,
        spaceBefore=16,
        fontName='Helvetica-Bold',
    ))
    styles.add(ParagraphStyle(
        name='EagleH3',
        parent=styles['Heading3'],
        fontSize=12,
        textColor=nci_blue,
        spaceAfter=8,
        spaceBefore=12,
        fontName='Helvetica-Bold',
    ))
    styles.add(ParagraphStyle(
        name='EagleH4',
        parent=styles['Heading4'],
        fontSize=11,
        textColor=nci_blue,
        spaceAfter=6,
        spaceBefore=10,
        fontName='Helvetica-BoldOblique',
    ))
    styles.add(ParagraphStyle(
        name='EagleCode',
        parent=styles['Code'],
        fontSize=8,
        fontName='Courier',
        backColor=colors.Color(0.95, 0.95, 0.95),
        borderColor=colors.Color(0.85, 0.85, 0.85),
        borderWidth=0.5,
        borderPadding=4,
    ))
    styles.add(ParagraphStyle(
        name='EagleBullet',
        parent=styles['Normal'],
        leftIndent=24,
        bulletIndent=12,
        spaceBefore=2,
        spaceAfter=2,
    ))
    styles.add(ParagraphStyle(
        name='EagleBlockquote',
        parent=styles['Normal'],
        leftIndent=24,
        textColor=colors.Color(0.35, 0.35, 0.35),
        fontName='Helvetica-Oblique',
        fontSize=10,
        borderColor=nci_blue,
        borderWidth=2,
        borderPadding=(8, 4, 4, 12),
        spaceBefore=6,
        spaceAfter=6,
    ))

    story = []

    # ── Title block ──────────────────────────────────────────────────
    story.append(Paragraph(_escape_html(title), styles['EagleTitle']))
    story.append(Paragraph("National Cancer Institute (NCI)", styles['EagleSubtitle']))
    story.append(Paragraph(
        f"Document Date: {datetime.utcnow().strftime('%B %d, %Y')}",
        styles['EagleSubtitle'],
    ))
    story.append(Paragraph("<b>DRAFT — FOR REVIEW ONLY</b>", styles['EagleStatus']))
    story.append(Spacer(1, 12))

    # ── Parse markdown ───────────────────────────────────────────────
    lines = content.split('\n')
    in_code_block = False
    code_lines: List[str] = []
    in_table = False
    table_data: List[List[str]] = []
    for line in lines:
        stripped = line.strip()

        # Code blocks
        if stripped.startswith('```'):
            if in_code_block:
                if code_lines:
                    code_text = '<br/>'.join(_escape_html(cl) for cl in code_lines)
                    story.append(Paragraph(code_text, styles['EagleCode']))
                    story.append(Spacer(1, 6))
                code_lines = []
                in_code_block = False
            else:
                in_code_block = True
            continue

        if in_code_block:
            code_lines.append(line)
            continue

        # Tables
        if stripped.startswith('|') and stripped.endswith('|'):
            if '---' in stripped:
                continue
            cells = [c.strip() for c in stripped.split('|')[1:-1]]
            if not in_table:
                in_table = True
                table_data = [cells]
            else:
                table_data.append(cells)
            continue
        elif in_table:
            _add_table_to_pdf(story, table_data, styles, nci_blue, nci_light)
            table_data = []
            in_table = False

        # Headers
        if stripped.startswith('# '):
            story.append(Paragraph(_escape_html(stripped[2:]), styles['EagleH1']))
        elif stripped.startswith('## '):
            story.append(Paragraph(_escape_html(stripped[3:]), styles['EagleH2']))
        elif stripped.startswith('### '):
            story.append(Paragraph(_escape_html(stripped[4:]), styles['EagleH3']))
        elif stripped.startswith('#### '):
            story.append(Paragraph(_escape_html(stripped[5:]), styles['EagleH4']))
        # Horizontal rules
        elif stripped in ('---', '***', '___'):
            from reportlab.platypus import HRFlowable
            story.append(HRFlowable(
                width="100%", thickness=1,
                color=colors.Color(0.8, 0.8, 0.8),
                spaceBefore=8, spaceAfter=8,
            ))
        # Checkboxes (before bullets — '- [ ] ' starts with '- ')
        elif stripped.startswith('- [ ] '):
            story.append(Paragraph(
                f"☐  {_markdown_to_reportlab(stripped[6:])}",
                styles['EagleBullet'],
            ))
        elif stripped.startswith('- [x] ') or stripped.startswith('- [X] '):
            story.append(Paragraph(
                f"☑  {_markdown_to_reportlab(stripped[6:])}",
                styles['EagleBullet'],
            ))
        # Bullet points
        elif stripped.startswith('- ') or stripped.startswith('* '):
            story.append(Paragraph(
                f"•  {_markdown_to_reportlab(stripped[2:])}",
                styles['EagleBullet'],
            ))
        # Numbered lists (preserve the number)
        elif re.match(r'^(\d+)\.\s', stripped):
            m = re.match(r'^(\d+)\.\s(.*)', stripped)
            num = m.group(1)
            text = m.group(2)
            story.append(Paragraph(
                f"{num}.  {_markdown_to_reportlab(text)}",
                styles['EagleBullet'],
            ))
        # Blockquotes
        elif stripped.startswith('> '):
            story.append(Paragraph(
                _markdown_to_reportlab(stripped[2:]),
                styles['EagleBlockquote'],
            ))
        # Empty lines
        elif not stripped:
            story.append(Spacer(1, 6))
        # Regular paragraphs
        else:
            text = _markdown_to_reportlab(stripped)
            story.append(Paragraph(text, styles['Normal']))

    # Remaining table
    if in_table and table_data:
        _add_table_to_pdf(story, table_data, styles, nci_blue, nci_light)

    # Build
    doc.build(story, onFirstPage=_on_page, onLaterPages=_on_page)
    buffer.seek(0)
    return buffer.read()


def _add_table_to_pdf(story, table_data: list, styles, nci_blue, nci_light):
    """Add a branded table to the PDF story."""
    from reportlab.platypus import Table, TableStyle, Paragraph, Spacer
    from reportlab.lib import colors

    if not table_data:
        return

    wrapped_data = []
    for row in table_data:
        wrapped_row = [Paragraph(_escape_html(cell), styles['Normal']) for cell in row]
        wrapped_data.append(wrapped_row)

    table = Table(wrapped_data, repeatRows=1)

    style_cmds = [
        # Header row — NCI blue background, white bold text
        ('BACKGROUND', (0, 0), (-1, 0), nci_blue),
        ('TEXTCOLOR', (0, 0), (-1, 0), colors.whitesmoke),
        ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
        ('FONTSIZE', (0, 0), (-1, 0), 10),
        ('BOTTOMPADDING', (0, 0), (-1, 0), 8),
        ('TOPPADDING', (0, 0), (-1, 0), 6),
        # Body rows
        ('ALIGN', (0, 0), (-1, -1), 'LEFT'),
        ('FONTSIZE', (0, 1), (-1, -1), 9),
        ('TOPPADDING', (0, 1), (-1, -1), 4),
        ('BOTTOMPADDING', (0, 1), (-1, -1), 4),
        # Grid
        ('GRID', (0, 0), (-1, -1), 0.5, colors.Color(0.75, 0.75, 0.75)),
        ('LINEBELOW', (0, 0), (-1, 0), 1.5, nci_blue),
    ]

    # Alternate row shading
    for i in range(1, len(table_data)):
        if i % 2 == 0:
            style_cmds.append(('BACKGROUND', (0, i), (-1, i), nci_light))

    table.setStyle(TableStyle(style_cmds))
    story.append(table)
    story.append(Spacer(1, 12))


def _escape_html(text: str) -> str:
    """Escape HTML special characters for reportlab Paragraph markup."""
    return (text
            .replace('&', '&amp;')
            .replace('<', '&lt;')
            .replace('>', '&gt;'))


def _markdown_to_reportlab(text: str) -> str:
    """Convert markdown inline formatting to reportlab XML tags."""
    text = _escape_html(text)
    # Bold
    text = re.sub(r'\*\*(.+?)\*\*', r'<b>\1</b>', text)
    # Italic
    text = re.sub(r'\*(.+?)\*', r'<i>\1</i>', text)
    # Code
    text = re.sub(r'`(.+?)`', r'<font name="Courier" color="#505050">\1</font>', text)
    return text


def _pdf_fallback(content: str, title: str) -> bytes:
    """Fallback when reportlab is not available."""
    header = f"EAGLE Document Export (PDF fallback - text format)\nTitle: {title}\nGenerated: {datetime.utcnow().isoformat()}\n\n{'=' * 60}\n\n"
    return (header + content).encode('utf-8')


